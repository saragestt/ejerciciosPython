numero1 = int(input("Intrudice el primer numero:\n"))
numero2 = int(input("Introduce el segundo numero:\n"))
operador = input("Introduce un operador(+,-,*,/):\n")

if operador == "+":
    print(numero1 + numero2)
elif operador == "-":
    print(numero1 - numero2)
elif operador == "*":
    print(numero1 * numero2)
elif operador == "/":
    print(numero1 / numero2)


